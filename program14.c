{
	int iCnt = 0;

//1 Initialization
//2 Condition
//3 Displacement
//Loop Body

//          1         2     3
	for (iCnt = 0;iCnt<5;iCnt++)
	{
		printf("Hello\n");    //4
	}
}
int main()
{
	Display();		//Function Call
	return 0;
}
